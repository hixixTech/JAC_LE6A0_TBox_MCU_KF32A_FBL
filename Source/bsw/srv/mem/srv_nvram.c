/**
 * Function Name: ApiNvramReadSyncInd
 * Description: 从ram读ee数据
 *
 * Param:   none
 * Return:  none
 * Author:  2021/06/22, feifei.xu create this function
 ****************************************************************************/
BOOL ApiNvramReadSyncInd(UINT32 u32_id, UINT16 u16_len,UINT8* p_u8_buff) 
{
    return Mem_EE_Read(u32_id,p_u8_buff,u16_len);
}

/****************************************************************************/
/**
 * Function Name: ApiNvramWritAsyncInd
 * Description: 向ram写ee数据，同步更新flash
 *
 * Param:   none
 * Return:  none
 * Author:  2021/06/22, feifei.xu create this function
 ****************************************************************************/
BOOL ApiNvramWritAsyncInd(UINT32 u32_id, UINT16 u16_len,UINT8* p_u8_buff)
{
    return Mem_EE_write(u32_id,p_u8_buff,u16_len);
}

