BOOL Ecu_Misc_CompareByte(const UINT8* u8_src,const UINT8 u8_data,UINT16 u16_len)
{
	UINT16 u16_i = 0;
	for(u16_i = 0; u16_i < u16_len; u16_i++)
	{
		if(u8_src[u16_i] != u8_data)
		{
			return FALSE;
		}
	}
	return TRUE;
}

